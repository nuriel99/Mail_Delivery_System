package model;

public class PersonDetails 
{
    String id;
    int inLine;
    int startWaitInMinute;
    int startWaitInHour;
    int endWaitInMinute;
    int endWaitInHour;
    int waitTime;
	public String getId() {
		return id;
	}
	public int getInLine() {
		return inLine;
	}
	public int getStartWaitInMinute() {
		return startWaitInMinute;
	}
	public int getStartWaitInHour() {
		return startWaitInHour;
	}
	public int getEndWaitInMinute() {
		return endWaitInMinute;
	}
	public int getEndWaitInHour() {
		return endWaitInHour;
	}
	public int getWaitTime() {
		return waitTime;
	}
    
    
}